package com.acme.app.dto.device;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

/**
 * 返回对象
 * @author YL.Z
 *
 * @date 2017年3月30日 下午7:51:15
 */
public class DeviceDto implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -157714521492742819L;

	private Long id;

    private String title;

    private String type;//设备类型：

    private String about;

    private BigDecimal lat;

    private BigDecimal lng;

    private Date gmtCreate;//创建时间
    
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}


	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAbout() {
		return about;
	}

	public void setAbout(String about) {
		this.about = about;
	}

	public BigDecimal getLat() {
		return lat;
	}

	public void setLat(BigDecimal lat) {
		this.lat = lat;
	}

	public BigDecimal getLng() {
		return lng;
	}

	public void setLng(BigDecimal lng) {
		this.lng = lng;
	}
	
	public Date getGmtCreate() {
		return gmtCreate;
	}

	public void setGmtCreate(Date gmtCreate) {
		this.gmtCreate = gmtCreate;
	}

	@Override
	public String toString() {
		return "DeviceDto [id=" + id + ", title=" + title + ", type=" + type + ", about=" + about + ", lat=" + lat
				+ ", lng=" + lng + ", gmtCreate=" + gmtCreate + "]";
	}

	
}
