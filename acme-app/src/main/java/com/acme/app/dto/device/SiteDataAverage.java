package com.acme.app.dto.device;

import java.io.Serializable;

/**
 * 近一个月的平均值
 * @author YL.Z
 *
 * @date 2017年4月8日 下午1:17:36
 */
public class SiteDataAverage implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -800431338945164902L;
	
	private int score;
	
	private String wph;

	private String nhthn;

	private String codmn;

	private String wdo;


	public int getScore() {
		return score;
	}


	public void setScore(int score) {
		this.score = score;
	}


	public String getWph() {
		return wph;
	}


	public void setWph(String wph) {
		this.wph = wph;
	}


	public String getNhthn() {
		return nhthn;
	}


	public void setNhthn(String nhthn) {
		this.nhthn = nhthn;
	}


	public String getCodmn() {
		return codmn;
	}


	public void setCodmn(String codmn) {
		this.codmn = codmn;
	}


	public String getWdo() {
		return wdo;
	}


	public void setWdo(String wdo) {
		this.wdo = wdo;
	}
	
	
}
